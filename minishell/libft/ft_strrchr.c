#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	index;

	index = ft_strlen(str);
	while (0 <= index)
	{	
		if (str[index] == c)
			return ((char *)&str[index]);
		index--;
	}
	return (NULL);
}
