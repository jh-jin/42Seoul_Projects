#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*list_ptr;

	list_ptr = lst;
	while (list_ptr != NULL && list_ptr->next != NULL)
		list_ptr = list_ptr->next;
	return (list_ptr);
}
