#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*ret;
	size_t			len;

	if (!s || !f)
		return (0);
	len = ft_strlen(s);
	ret = malloc(sizeof(char) * len + 1);
	if (!(ret))
		return (0);
	i = 0;
	while (s[i])
	{
		ret[i] = f(i, s[i]);
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
