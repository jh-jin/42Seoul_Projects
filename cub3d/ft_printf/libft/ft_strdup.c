#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*str;
	char	*dest;

	i = 0;
	str = (char *)s1;
	while (str[i])
		i++;
	dest = malloc(sizeof(char) * i + 1);
	if (!(dest))
		return (0);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
